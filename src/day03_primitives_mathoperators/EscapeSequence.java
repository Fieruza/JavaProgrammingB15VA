package day03_primitives_mathoperators;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello\tB15");   // \t= 1 tab space
        System.out.println("Hello\t\tB15 Friends");  // \t=2 tab space

        //I love "java" programming \"\"
        System.out.println("I love \"Java\" programming");

        // Let's buy "coffee" and "tea"
        System.out.println("Let's buy \"coffee\" and \"tea\" ");

        //Java
        //Selenium
        System.out.println("Java\nSelenium");  // \n= takes you to a new line without letting you print statement twice

        //Print this: "\_/"
        System.out.println("\"\\_/\" ");




    }
}
